<h2>Welcome to Pails!</h2>

<p>
  Pails is the lightest, most powerful PHP MVC framework for rapid application
  development.
</p>

<p>
  More documentation can be found
  <a href="https://github.com/bparks/pails">on GitHub</a>
  or in Brian's <a href="http://bparks.github.io/">original blog post</a>.
  If you like pails, <a href="mailto:bparks@synapsesoftware.com">tell us
  about it</a>!
</p>

<hr />

<div class="row">
  <div class="col-md-4">
    <h3>Install some plugins</h3>
    <p>Browse the
      <a href="https://github.com/bparks/pails-plugins">plugin directory</a>
      for useful pieces of functionality you can use in your own apps.</p>
  </div>
  <div class="col-md-4">
    <h3>Craft your app</h3>
    <p>Put your models in the <code>models</code> directory, your controllers
    in the <code>controllers</code> directory, and views in the <code>views</code>
    directory. These are the only rules. Other than that, all valid PHP is
    permitted in pails.</p>
  </div>
  <div class="col-md-4">
    <h3>Deploy anywhere</h3>
    <p>Pails works on the PHP development server, apache mod_php5, nginx with
      fastcgi, and even on HHVM. This means you can host a pails app on
      <strong>any</strong> available PHP hosting service.</p>
  </div>
</div>
